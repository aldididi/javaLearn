public class
forLoop {
    public static void main(String[] args) {

        //loop with init statement
        for ( var count=1; count<=10;) {
            System.out.println("count" + count);
            count++;
        }
        System.out.println("=====");
        //loop with post statement
        for ( var count=1; count<=10;  count++) {
            System.out.println("count" + count);

        }

    }
}
